public class Ubicacion {

    //Atributos
    private int fila;
    private int columna;
    
    //Metodos
    public Ubicacion(int fil, int col){
        fila = fil;
        columna = col;
    }

    public int getFila(){
        return fila;
    }

    public int getColumna(){
        return columna;
    }

}